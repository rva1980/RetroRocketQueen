    private void GenerateStars()
    {
        

        for (int x = -20; x < 20; x++)
        {
            for (int y = -11; y < 11; y++)
            {
                Vector2Int actual = new Vector2Int(x, y);

                List<Vector2Int> excluidos = new List<Vector2Int>();

                switch (scene)
                {
                    case Scene.StartMenu:
                        excluidos = _excluidosStartMenu;
                        break;
                    case Scene.Options:
                        excluidos = _excluidosOptions;
                        break;
                    case Scene.Scores:
                        excluidos = _excluidosScores;
                        break;
                    case Scene.WebVersion:
                        excluidos = _excluidosWebVersion;
                        break;
                    default:
                        break;
                }

                if (!excluidos.Contains(actual))
                {
                    int probabilityStars = Random.Range(0, 99);

                    if (probabilityStars < 5)
                    {
                        starsTilemap.SetTile(new Vector3Int(x, y, 0), starsRuleTile);
                    }
                    else if (probabilityStars < 8)
                    {

                        starsTilemap.SetTile(new Vector3Int(x, y, 0), smallStarAnimatedTile);
                    }
                    else if (probabilityStars < 10)
                    {
                        starsTilemap.SetTile(new Vector3Int(x, y, 0), bigStarAnimatedTile);
                    }
                }
            }
        }
    }
